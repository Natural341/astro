// Combined exports
export const minorArcana: TarotCard[] = [...cupsCards, ...pentaclesCards, ...swordsCards, ...wandsCards];
export const fullDeck: TarotCard[] = [...majorArcana, ...minorArcana];

export const getRandomCards = (count: number = 3, deckType: 'major' | 'full' = 'major'): TarotCard[] => {
  const deck = deckType === 'full' ? fullDeck : majorArcana;
  const shuffled = [...deck].sort(() => Math.random() - 0.5);
  return shuffled.slice(0, count);
};

export const getCardById = (id: number): TarotCard | undefined => {
  return fullDeck.find((card) => card.id === id);
};

export const getAllCards = (): TarotCard[] => {
  return fullDeck;
};
